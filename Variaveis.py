# Variaveis
a = 10        # <class 'int'>
b = 2.0       # <class 'float'>
c = 'a'       # <class 'str'>
d = "Python"  # <class 'str'>
e = True      # <class 'bool'>
# tipos default (Padrão) no Python, caso não seja especificado
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))

# Desafio
variavel_1 = 25.87
variavel_2 = True
variavel_3 = 'Bom dia!'
variavel_4 = 15
# O f antes da String permite incluir {} para variaveis
print(f"A variavel 1: {variavel_1}, é de: {type(variavel_1)}")
print(f"A variavel 2: {variavel_2}, é de: {type(variavel_2)}")
print(f"A variavel 3: {variavel_3}, é de: {type(variavel_3)}")
print(f"A variavel 4: {variavel_4}, é de: {type(variavel_4)}")
